		public void GetFedResMeetSeries ( int FedSeries )
		{
			string[] tokens = FedResMeetingString.Split ( ",".ToCharArray ( ) );
			foreach ( string token in tokens )
			{
				string datestr = token.Trim ( );
				if ( datestr == "" )
				{
					continue;
				}
				DateTime date = IntDateToDateTime ( Int32.Parse ( datestr ) );
				int bar = DateToBar ( date );
				if ( bar != -1 )
				{
					WL.SetSeriesValue ( bar, FedSeries, 300 );
				}
			}
		}

